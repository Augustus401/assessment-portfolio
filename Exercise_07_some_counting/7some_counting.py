print('counting up from 0 to 50')
for a in range(1, 51):
    print(a)
print("---------------------------------------------------------------")
print('counting down from 50 to 0')
for b in range(50, 0, -1):
    print(b)
print("---------------------------------------------------------------")
print('counting up from 30 to 50')
for c in range(30, 51):
    print(c)
print("---------------------------------------------------------------")
print('counting down from 50 to 10 by 2')
for d in range(50, 9, -2):
    print(d)
print("---------------------------------------------------------------")
